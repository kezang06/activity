for i in range(4):
    if i == 2:
        break #the break statement is used to exit the loop when the value of i is 2
    print(i)
print("Loop ended.")

for i in range(4):
    if i == 2:
        continue #the continue statement is used to skip the current iteration when the value of i is 2
    print(i)
print("Loop ended.")
